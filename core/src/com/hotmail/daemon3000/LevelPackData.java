package com.hotmail.daemon3000;

public class LevelPackData {
	public String name;
	public String id;
	public String file;
	public int levelCount;
	
	public LevelPackData(String name, String id, String file, int levelCount) {
		this.name = name;
		this.file = file;
		this.id = id;
		this.levelCount = levelCount;
	}
}
